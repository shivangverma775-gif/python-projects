grade = int(input("Please enter the marks: "))

if grade >= 90 and grade <= 100:
    print("You got Ex grade")
elif grade >= 80:
    print("You got A grade")
elif grade >= 70:
    print("You got B grade")
elif grade >= 60:
    print("You got C grade")
elif grade >= 50:
    print("You got D grade")
else:
    print("You got F grade")
